<!-- Indexed array -->
$fruits = ['apple', 'banana', 'orange'];
echo $fruits[0]; // Output: apple
<!-- Associative array -->
$person = ['name' => 'John', 'age' => 30, 'location' => 'New York'];
echo $person['name']; // Output: John

